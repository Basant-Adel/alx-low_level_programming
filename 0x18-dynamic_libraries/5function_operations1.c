#include "sum.h"

/**
 * add - A function to Adds two integers
 *@a: It's a First integer
 *@b: It's a Second integer
 *Return: Sum of a and b
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - A function to Subtracts two integers
 *@a: It's a First integer
 *@b: It's a Second integer
 *Return: Difference of a and b
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - A function to Multiplies two integers
 *@a: It's a First integer
 *@b: It's a Second integer
 *Return: Product of a and b
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - A function to Divides two integers
 *@a: It's a First integer
 *@b: It's a Second integer
 *Return: Quotient of a and b
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - A function to Modulus of two integers
 *@a: It's a First integer
 *@b: It's a Second integer
 *Return: Remainder of a and b
*/

int mod(int a, int b)
{
	return (a % b);
}
